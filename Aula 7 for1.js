let contador = 1
while (contador <= 10) {
    console.log(`Contador while = ${contador}`)
    contador++
}

for (let i = 1; i <= 10; i++) {
    console.log(`Contador for ${i}`)
}

const notas = [6.7, 8.8, 9.0, 10.0, 2.3]
